// for

for (x=1; x<=20; x++) {
    console.log(x)
}

for (y=20; y>=1; y--) {
    console.log(y)
}

for (n=2; n<=100; n=n+2) {
    console.log(n)
}

for (z=1; z<=99; z=z+2) {
    console.log(z)
}

// while

x=1
while (x<=20) {
    console.log(x)
    x++
}

y=20
while (y>=1) {
    console.log(y)
    y--
}

n=2
while (n<=100) {
    console.log(n)
    n = n+2
}

z=1
while (z<=99) {
    console.log(z)
    z = z+2
}

